def setup_sequences(connection):
    """시퀀스 생성 (필요시)"""
    cursor = connection.cursor()
    
    print("🔢 시퀀스 생성 중...")
    
    try:
        cursor.execute("""
            CREATE SEQUENCE etf_signal_seq
            START WITH 1
            INCREMENT BY 1
            NOCACHE
        """)
        print("  ✅ etf_signal_seq 시퀀스 생성 완료")
    except Exception as e:
        print(f"  ⚠️ 시퀀스 생성 실패 (이미 존재할 수 있음): {e}")
    
    cursor.close()

def main():
    """메인 함수"""
    print("🚀 ETF 자동매매 시스템 데이터베이스 설정 시작")
