#!/usr/bin/env python3
"""
ETF 자동매매 시스템 데이터베이스 설정
Oracle 데이터베이스에 필요한 테이블과 인덱스를 생성합니다.
"""

import cx_Oracle
import yaml
import sys
import os

def load_config():
    """설정 파일 로드"""
    with open('config.yaml', 'r', encoding='utf-8') as f:
        return yaml.safe_load(f)

def connect_database(config):
    """데이터베이스 연결"""
    db_config = config['database']
    
    dsn = cx_Oracle.makedsn(
        db_config['host'], 
        db_config['port'], 
        service_name=db_config['service']
    )
    
    connection = cx_Oracle.connect(
        db_config['user'],
        db_config['password'], 
        dsn
    )
    
    return connection

def create_tables(connection):
    """테이블 생성"""
    cursor = connection.cursor()
    
    print("📊 ETF 마스터 테이블 생성 중...")
    cursor.execute("""
        CREATE TABLE etf_master (
            etf_code VARCHAR2(10) PRIMARY KEY,
            etf_name VARCHAR2(100) NOT NULL,
            underlying_index VARCHAR2(100),
            category VARCHAR2(50),
            expense_ratio NUMBER(5,4),
            nav NUMBER(12,2),
            created_date DATE DEFAULT SYSDATE,
            updated_date DATE DEFAULT SYSDATE
        )
    """)
    
    print("📈 ETF 일별 가격 테이블 생성 중...")
    cursor.execute("""
        CREATE TABLE etf_daily_price (
            etf_code VARCHAR2(10),
            trade_date DATE,
            open_price NUMBER(10,2),
            high_price NUMBER(10,2),
            low_price NUMBER(10,2),
            close_price NUMBER(10,2),
            volume NUMBER(15),
            nav NUMBER(10,2),
            tracking_error NUMBER(6,4),
            created_date DATE DEFAULT SYSDATE,
            PRIMARY KEY (etf_code, trade_date),
            FOREIGN KEY (etf_code) REFERENCES etf_master(etf_code)
        )
    """)
    
    print("🧮 ETF 기술적 지표 테이블 생성 중...")
    cursor.execute("""
        CREATE TABLE etf_technical_indicators (
            etf_code VARCHAR2(10),
            trade_date DATE,
            close_price NUMBER(10,2),
            sma_5 NUMBER(10,2),
            sma_20 NUMBER(10,2),
            sma_60 NUMBER(10,2),
            sma_200 NUMBER(10,2),
            ema_5 NUMBER(10,2),
            ema_20 NUMBER(10,2),
            ema_60 NUMBER(10,2),
            ema_200 NUMBER(10,2),
            rsi_14 NUMBER(5,2),
            macd_line NUMBER(10,4),
            macd_signal NUMBER(10,4),
            macd_histogram NUMBER(10,4),
            bb_upper NUMBER(10,2),
            bb_middle NUMBER(10,2),
            bb_lower NUMBER(10,2),
            bb_width NUMBER(10,4),
            volume NUMBER(15),
            volume_sma_20 NUMBER(15),
            created_date DATE DEFAULT SYSDATE,
            updated_date DATE DEFAULT SYSDATE,
            PRIMARY KEY (etf_code, trade_date),
            FOREIGN KEY (etf_code) REFERENCES etf_master(etf_code)
        )
    """)
    
    print("🎯 매매 신호 테이블 생성 중...")
    cursor.execute("""
        CREATE TABLE etf_trading_signals (
            signal_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            etf_code VARCHAR2(10),
            signal_date DATE,
            signal_type VARCHAR2(10),
            signal_strength VARCHAR2(10),
            price NUMBER(10,2),
            signal_reason VARCHAR2(500),
            indicators_json CLOB,
            created_date DATE DEFAULT SYSDATE,
            FOREIGN KEY (etf_code) REFERENCES etf_master(etf_code)
        )
    """)
    
    print("⚡ 실시간 가격 테이블 생성 중...")
    cursor.execute("""
        CREATE TABLE etf_realtime_price (
            etf_code VARCHAR2(10) PRIMARY KEY,
            current_price NUMBER(10,2),
            open_price NUMBER(10,2),
            high_price NUMBER(10,2),
            low_price NUMBER(10,2),
            volume NUMBER(15),
            change_rate NUMBER(8,4),
            update_time DATE,
            FOREIGN KEY (etf_code) REFERENCES etf_master(etf_code)
        )
    """)
    
    print("📊 ETF 구성종목 테이블 생성 중...")
    cursor.execute("""
        CREATE TABLE etf_holdings (
            etf_code VARCHAR2(10),
            stock_code VARCHAR2(10),
            stock_name VARCHAR2(100),
            weight_ratio NUMBER(6,4),
            shares NUMBER(15),
            market_value NUMBER(15),
            update_date DATE,
            PRIMARY KEY (etf_code, stock_code, update_date),
            FOREIGN KEY (etf_code) REFERENCES etf_master(etf_code)
        )
    """)
    
    cursor.close()

def create_indexes(connection):
    """인덱스 생성"""
    cursor = connection.cursor()
    
    print("🔍 인덱스 생성 중...")
    
    indexes = [
        "CREATE INDEX idx_etf_price_date ON etf_daily_price(trade_date)",
        "CREATE INDEX idx_tech_etf_date ON etf_technical_indicators(etf_code, trade_date DESC)",
        "CREATE INDEX idx_tech_date ON etf_technical_indicators(trade_date)",
        "CREATE INDEX idx_signals_etf_date ON etf_trading_signals(etf_code, signal_date DESC)",
        "CREATE INDEX idx_signals_type ON etf_trading_signals(signal_type, signal_strength)",
        "CREATE INDEX idx_holdings_date ON etf_holdings(update_date)",
        "CREATE INDEX idx_realtime_update ON etf_realtime_price(update_time)"
    ]
    
    for index_sql in indexes:
        try:
            cursor.execute(index_sql)
            print(f"  ✅ {index_sql.split()[2]} 생성 완료")
        except Exception as e:
            print(f"  ⚠️ 인덱스 생성 실패: {e}")
    
    cursor.close()

